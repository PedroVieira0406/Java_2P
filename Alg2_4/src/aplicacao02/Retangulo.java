package aplicacao02;

public class Retangulo {
	
	private int largura;
	private int altura;
	
	public Retangulo (int altura, int largura) {
		this.largura = largura;
		this.altura = altura;
		
	}
	
	public int getLargura() {
		return largura;
	}
	public void setLargura(int largura) {
		this.largura = largura;
	}
	public int getAltura() {
		return altura;
	}
	public void setAltura(int altura) {
		this.altura = altura;
	}

}
